#include "sh.h"

int		ft_detect_tab(char *buffer)
{
	if (buffer[0] == 9 && buffer[1] == 0 && buffer[2] == 0 &&
		buffer[3] == 0 && buffer[4] == 0 && buffer[5] == 0)
		return (1);
	return (0);
}
