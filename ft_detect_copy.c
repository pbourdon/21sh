#include "sh.h"

int		ft_detect_copy(char *buffer)
{
	if (buffer[0] == -61 && buffer[1] == -89 && buffer[2] == 0 && buffer[3] == 0
		&& buffer[4] == 0 && buffer[5] == 0)
	{
		return (1);
	}
	return (0);
}
