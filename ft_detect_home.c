#include "sh.h"

int		ft_detect_home(char *buf)
{
	if (buf[0] == 27 && buf[1] == 91 && buf[2] == 72 &&
		buf[3] == 0 && buf[4] == 0 && buf[5] == 0)
		return (1);
	return (0);
}

